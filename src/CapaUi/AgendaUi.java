    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tablaContactos.getSelectedRow(); 
    if (fila >= 0) { 
        modelo.removeRow(fila); 
    } else { 
        JOptionPane.showMessageDialog(this, "Seleccione un contacto para eliminar."); 
  }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String criterio = txtBuscar.getText().trim().toLowerCase();
    boolean encontrado = false;

    for (int i = 0; i < modelo.getRowCount(); i++) {
        String nombre = modelo.getValueAt(i, 0).toString().toLowerCase();
        String telefono = modelo.getValueAt(i, 1).toString().toLowerCase();

        if (nombre.contains(criterio) || telefono.contains(criterio)) {
            tablaContactos.setRowSelectionInterval(i, i);
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        JOptionPane.showMessageDialog(this, "Contacto no encontrado.");
        tablaContactos.clearSelection();
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

